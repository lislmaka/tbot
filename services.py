import json


# ------------------------------------------------------------------------------- #
#
# ------------------------------------------------------------------------------- #
class services():
    # --------------------------------------------------------------------------- #
    #
    # --------------------------------------------------------------------------- #
    def __init__(self):
        """ Init """
        self.log_path = "logs/{}.txt"
        self.config_env = None

        self.config_load()
        self.is_log = self.get_config()["log"]

    # --------------------------------------------------------------------------- #
    #
    # --------------------------------------------------------------------------- #
    def logs(self, msg, file_name):
        """
        """
        if self.is_log:
            url = self.log_path.format(file_name)
            with open(url, 'a') as file:
                print(msg, file=file)

    # --------------------------------------------------------------------------- #
    #
    # --------------------------------------------------------------------------- #
    def config_load(self):
        """
        """
        with open("config_env.json") as json_file:
            self.config_env = json.load(json_file)

    # --------------------------------------------------------------------------- #
    #
    # --------------------------------------------------------------------------- #
    def get_config(self):
        """
        """
        if self.config_env:
            return self.config_env
        else:
            self.config_load()
            return self.config_env